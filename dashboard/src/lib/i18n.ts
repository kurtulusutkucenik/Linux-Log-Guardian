/** Basit dil tespiti — kullanıcı mesajı ile aynı dilde yanıt için */
export function detectTextLocale(text: string): Locale {
  const s = text.trim();
  if (!s) return "en";
  const trChars = /[ğüşöçıİĞÜŞÖÇ]/;
  const deChars = /[äöüßÄÖÜ]/;
  const frChars = /[àâçéèêëîïôùûüœæÀÂÇÉÈÊËÎÏÔÙÛÜŒÆ]/;
  const ruChars = /[\u0400-\u04FF]/;
  const zhChars = /[\u4e00-\u9fff]/;
  const jaChars = /[\u3040-\u30ff]/;
  const koChars = /[\uac00-\ud7af]/;
  const arChars = /[\u0600-\u06FF]/;
  if (trChars.test(s)) return "tr";
  if (arChars.test(s)) return "ar";
  if (zhChars.test(s)) return "zh";
  if (jaChars.test(s)) return "ja";
  if (koChars.test(s)) return "ko";
  if (ruChars.test(s)) return "ru";
  if (deChars.test(s)) return "de";
  if (frChars.test(s)) return "fr";
  const lower = s.toLowerCase();
  const trWords =
    /\b(ve|bir|için|olan|nedir|nasıl|tehdit|ban|özet|sistem|güvenlik|merhaba|evet|hayır)\b/i;
  if (trWords.test(lower)) return "tr";
  const ruWords =
    /\b(и|или|что|как|угроз|бан|систем|безопас|привет|да|нет|флот|агент|отчет|отчёт|ip|блок)\b/i;
  if (ruWords.test(lower)) return "ru";
  return "en";
}

export function localeInstruction(locale: Locale): string {
  const names: Record<Locale, string> = {
    tr: "Turkce",
    en: "English",
    de: "Deutsch",
    fr: "Francais",
    ru: "Russkij",
    es: "Espanol",
    pt: "Portugues",
    it: "Italiano",
    pl: "Polski",
    nl: "Nederlands",
    ar: "Arabic",
    zh: "Chinese",
    ja: "Japanese",
    ko: "Korean",
  };
  if (locale === "ru") {
    return `ЯЗЫК ОТВЕТА: Пишите только на русском. Исключения: RCE, EPS, SIEM, IP, BAN_IP. Тон профессионального SOC.`;
  }
  if (locale === "ar" || locale === "zh" || locale === "ja" || locale === "ko") {
    return `RESPONSE LANGUAGE: Write only in ${names[locale]}. Exceptions: RCE, EPS, SIEM, IP, BAN_IP. Professional SOC tone.`;
  }
  return `YANIT DILI: Yalnizca ${names[locale]} yaz. Ingilizce kelime kullanma (RCE, EPS, SIEM, IP, BAN_IP istisna). Profesyonel SOC ussu.`;
}
